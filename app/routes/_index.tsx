import type { V2_MetaFunction } from "@remix-run/cloudflare";
import { gql, useQuery } from "@apollo/client";

export const meta: V2_MetaFunction = () => {
  return [
    { title: "New Remix App" },
    { name: "description", content: "Welcome to Remix!" },
  ];
};

